package sg.edu.nus.comp.InnTell.model;

public class HotelRank {
	
	int aorRank;
	
	int arrRank;
	
	int revRank;

	public int getAorRank() {
		return aorRank;
	}

	public void setAorRank(int aorRank) {
		this.aorRank = aorRank;
	}

	public int getArrRank() {
		return arrRank;
	}

	public void setArrRank(int arrRank) {
		this.arrRank = arrRank;
	}

	public int getRevRank() {
		return revRank;
	}

	public void setRevRank(int revRank) {
		this.revRank = revRank;
	}
	

}
